	<p style="font-family:"font-size:14px;text-indent:2em;color:#000000;">
		上海国改路线
	</p>
	<p style="font-family:"font-size:14px;text-indent:2em;color:#000000;">
		编者按：2月16日，作为国企改革排头兵的上海召开了国资国企年度工作会议，对2017年的工作进行部署，明确“深化国企改革发展混合所有制经济为首位”。上海国企改革市场化程度较高，被列为国企改革排头兵。改革伊始便多路径大尺度创新，从上海国资委联手央企为三爱富量身定做“三元并购”重组新模式，再到上周华鑫股份打破此前市场关于注入集团地产业务预期，选择注入证券类资产，这些密集浮现的案例都折射出上海国资改革已打破常规套路，以更市场化的重组方式盘活旗下资产。进入国企改革“施工期”，上海的动作被全国广泛关注，是继续求变还是选择求稳？正在等待上海给出答案。
	</p>
	<p style="font-family:"font-size:14px;text-indent:2em;color:#000000;">
		上海明确2017年要积极推进2-3家科研院所深化改革建立多元化投资、市场化经营机制，2-3家企业集团整体上市或核心业务资产上市，稳妥推进10家符合条件的混合所有制企业试点员工持股。
	</p>
	<p style="font-family:"font-size:14px;text-indent:2em;color:#000000;">
		上海新一轮深化国企改革的大幕已经开启。
	</p>
	<p style="font-family:"font-size:14px;text-indent:2em;color:#000000;">
		2月16日下午，备受关注的2017年上海国资大会召开，会议部署了新一年的上海国资改革工作重点，在包括发展混合所有制经济、完善国资运营平台运作机制以及健全激励机制等方面都有重要部署。
	</p>
	<p style="font-family:"font-size:14px;text-indent:2em;color:#000000;">
		在混改方面，上海明确2017年要积极推进2-3家科研院所深化改革建立多元化投资、市场化经营机制，2-3家企业集团整体上市或核心业务资产上市，稳妥推进10家符合条件的混合所有制企业试点员工持股等。
	</p>
	<p style="font-family:"font-size:14px;text-indent:2em;color:#000000;">
		中国企业改革研究院研究员、国资专家李锦在接受21世纪经济报道记者采访时表示，上海作为地方国资改革重镇，无论是在力度还是前瞻性上，都远远走在全国前列，但在传统产业的结构调整、上市公司壳资源利用、打造具有国际竞争力的企业上，还需要加大力度。
	</p>
	<p style="font-family:"font-size:14px;text-indent:2em;color:#000000;">
		深化混合所有制改革
	</p>
	<p style="font-family:"font-size:14px;text-indent:2em;color:#000000;">
		深化国企改革发展混合所有制经济在2017年上海国资工作任务中排在首位。
	</p>
	<p style="font-family:"font-size:14px;text-indent:2em;color:#000000;">
		上述会议提出，2017年上海将积极推进2-3家科研院所深化改革建立多元化投资、市场化经营机制，2-3家企业集团整体上市或核心业务资产上市。功能类、公共服务类企业中的竞争性业务资产，利用现有上市平台引入各类资本，提高资源配置效率和公共服务能力。
	</p>
	<p style="font-family:"font-size:14px;text-indent:2em;color:#000000;">
		还要稳妥推进10家符合条件的混合所有制企业试点员工持股。完成建科院集团增资扩股，引入战略投资者。以国家油气改革为契机，推进上海市燃气行业企业改革，适时实施混合所有制改革。指导集团公司推进所属企业的公司制改制。积极推进上海市供销社系统综合改革工作，完成联交所事转企改制，研究推进科研院所多元化改革。
	</p>
	<p style="font-family:"font-size:14px;text-indent:2em;color:#000000;">
		要放大资本市场正向效应，推动境内外优质资源主动对接资本市场，支持国有股东和上市公司通过证券市场进行股权运作、债券市场直接融资、产权市场盘活存量等方式提高资产收益。
	</p>
	<p style="font-family:"font-size:14px;text-indent:2em;color:#000000;">
		复旦大学企业研究所所长张晖明在接受21世纪经济报道记者采访时表示，从深化混改的角度出发，上海可以做好以下三方面的工作。一是“层次”，如根据分类特点，允许一些市属层次的企业可以整体上市；二是“对象”，择优选择战略投资者；三是优化股权结构，因为股权结构的科学安排是一个企业改制成长的重要起点，决定了后续的治理结构。
	</p>
	<p style="font-family:"font-size:14px;text-indent:2em;color:#000000;">
		在优化国资布局方面，上海曾提出，到“十三五”末将国资委系统85%以上的国资集中在战略性新兴产业、先进制造业与现代服务业、基础设施与民生保障等关键领域和优势产业。
	</p>
	<p style="font-family:"font-size:14px;text-indent:2em;color:#000000;">
