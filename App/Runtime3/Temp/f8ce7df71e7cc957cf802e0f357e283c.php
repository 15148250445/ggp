	</p>
	<p style="font-family:"font-size:14px;text-indent:2em;color:#000000;">
		李锦认为，在公司完成混合所有制改革之后，还要发挥国有企业各类人才积极性、主动性和创造性，激发各类要素活力。
	</p>
	<p style="font-family:"font-size:14px;text-indent:2em;color:#000000;">
		张晖明表示，在国有体系内，企业家的角色、定位发生了一定改变。“科学市场化给予了他新的责任，包括遴选机制、评价机制、奖惩机制、薪酬机制等，都需要对经营管理团队做出重新考量。除此以外，还需要考虑如何调动员工积极性，即员工持股的问题。”
	</p>
	<p style="font-family:"font-size:14px;text-indent:2em;color:#000000;">
		如何优化治理结构？此次上海国资大会指出，将“着力强化法人治理建设，健全选人用人机制”。具体而言，将实施国有企业领导人员分类分层管理，建立健全权责对等、运转协调、有效制衡的决策执行监督机制、健全“双向进入、交叉任职”企业领导体制；强化董事会建设，落实重大决策、选人用人、业绩考核、薪酬分配等权利；完善监事会建设；稳妥推进职业经理人薪酬制度；改革制定市管企业任期综合考核办法；加快人才队伍建设等。
	</p>
	<p style="font-family:"font-size:14px;text-indent:2em;color:#000000;">
		李锦对上海的职业经理人改革表现出期待：“因为从市场征集职业经理人，需要政府放权，由董事会聘用，各地对此比较慎重，有望在上海取得突破。”上海国资委表示将“一司一策制定方案，成熟一家推进一家，探索研究法定代表人政府奖励办法”。
	</p>
	<p style="font-family:"font-size:14px;text-indent:2em;color:#000000;">
		而针对员工持股计划，2017年1月发布的《关于本市地方国有控股混合所有制企业员工持股首批试点工作实施方案》，就曾明确指出将推进上海地方国有企业员工首批试点工作，选取5-10家企业开展首批试点，并对企业分类、股权结构、管理制度、营收来源等条件作出了限制。此次会议则进一步明确要“稳妥推进10家符合条件的混合所有制企业试点员工持股”。
	</p>
	<p style="font-family:"font-size:14px;text-indent:2em;color:#000000;">
		总体而言，李锦预计，2017年上海国企“二次混改”、引入社会资本、员工持股、核心资产或优良资产上市的步伐将明显加快。
	</p>
