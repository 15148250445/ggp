<h1>
	<span style="line-height:22px;"><span style="font-family:Arial;line-height:28px;"><span style="font-family:" line-height:28px;font-size:medium;"="">尊敬的交易商：</span></span></span> 
</h1>
<p style="font-size:14px;text-indent:2em;">
	<span style="line-height:22px;"><span style="font-family:Arial;line-height:28px;"><span style="font-family:" line-height:28px;font-size:medium;"="">欢迎您参加茶叶实物交易！为了让您充分了解交易风险，<span>众泰财富</span>商品交易中心 （以下简称“交易中心”）特提供本风险提示书。若您选择参与交易中心茶叶实物交易，则视为您已仔细阅读本风险提示书并自愿承担茶叶实物交易带来的风险。<br />
</span></span></span> 
</p>
<p style="font-size:14px;text-indent:2em;">
	<span style="line-height:22px;"><span style="font-family:Arial;line-height:28px;"><span style="font-family:" line-height:28px;font-size:medium;"="">鉴于交易商进行茶叶交易存在风险并可能导致交易商亏损，请您慎重考虑自身情况是否适合进行此类交易，并确定可以自行承担风险及亏损，同时客观理性地认识到茶叶交易的风险包括但不限于：<br />
</span></span></span> 
</p>
<p style="font-size:14px;text-indent:2em;">
	<span style="line-height:22px;"><span style="font-family:Arial;line-height:28px;"><span style="font-family:" line-height:28px;font-size:medium;"="">一、宏观经济风险：由于我国宏观经济形势的变化以及茶叶价值取向的变化，存在可能影响茶叶价格上下波动的情形；国际宏观经济形势变化也会对茶叶交易市场产生影响，存在导致茶叶交易价格上下波动的情形。<br />
</span></span></span> 
</p>
<p style="font-size:14px;text-indent:2em;">
	<span style="line-height:22px;"><span style="font-family:Arial;line-height:28px;"><span style="font-family:" line-height:28px;font-size:medium;"="">二、政策法规及市场风险：茶叶交易作为一种创新的实物交易模式，自身的交易规则还需要在实践中不断完善，国家法律、法规及政策的变化以及影响价格波动的其他因素出现，都可能影响茶叶交易价格；或者由于本交易中心根据国家法律、法规及政策变化等原因而对交易市场相关规则进行修订，可能影响交易主体资格、交易规则等各个方面的变化。上述法律法规、政策及本所规则的风险，均可能导致交易中心交易的茶叶价格异常波动。<br />
</span></span></span> 
</p>
<p style="font-size:14px;text-indent:2em;">
	<span style="line-height:22px;"><span style="font-family:Arial;line-height:28px;"><span style="font-family:" line-height:28px;font-size:medium;"="">三、鉴定评估风险：由于国内目前尚没有权威的鉴定评估机构和统一的鉴定估值程序及完备的鉴定估值方法，茶叶鉴定估值意见书仅供交易商参考，不作为交易商主张其他权益的依据。<br />
</span></span></span> 
</p>
<p style="font-size:14px;text-indent:2em;">
	<span style="line-height:22px;"><span style="font-family:Arial;line-height:28px;"><span style="font-family:" line-height:28px;font-size:medium;"="">四、技术风险：由于交易的进行、信息的显示及资金的划付均是运用电子通讯技术和电脑技术并通过互联网传输来实现的，这些技术存在被网络黑客和计算机病毒攻击的可能，或者通讯技术、电脑技术和相关软件具有存在缺陷的可能，互联网传输故障也可能造成您的交易指令无法及时传输，这些风险均可导致您的交易申报无法成交或者无法全部成交，或者转账资金不能及时到账、交易延迟、中止甚至无法完成交易等。<br />
</span></span></span> 
</p>
<p style="font-size:14px;text-indent:2em;">
	<span style="line-height:22px;"><span style="font-family:Arial;line-height:28px;"><span style="font-family:" line-height:28px;font-size:medium;"="">五、不可抗力风险：诸如地震、台风、火灾、水灾、战争、瘟疫、社会动乱等不可抗力因素可能导致茶叶交易系统的瘫痪；交易中心无法控制和不可预测的系统故障、设备故障、通讯故障、电力故障等也可能导致茶叶交易系统非正常运行甚至瘫痪；银行无法控制和不可预测的系统故障、设备故障、通讯故障、电力故障等也可能导致资金转账系统非正常运行甚至瘫痪，这些风险可能导致您的交易申报无法成交或者无法全部成交，或者转账资金不能及时到账等。<br />
</span></span></span> 
</p>
<p style="font-size:14px;text-indent:2em;">
	<span style="line-height:22px;"><span style="font-family:Arial;line-height:28px;"><span style="font-family:" line-height:28px;font-size:medium;"="">六、账号密码泄露风险：由于交易商使用计算机被病毒侵入、黑客攻击等导致的密码泄露、账号泄露或交易商的身份被冒用，可能导致无法正确下达申报指令、恶意虚假申报或申报失败、延迟、错误等。<br />
</span></span></span> 
</p>
<p style="font-size:14px;text-indent:2em;">
	<span style="line-height:22px;"><span style="font-family:Arial;line-height:28px;"><span style="font-family:" line-height:28px;font-size:medium;"="">七、交易商软硬件系统风险：交易商的电脑设备及软件系统与所提供的网上交易系统不相匹配，导致无法下达申报指令或申报失败、延迟等。<br />
</span></span></span> 
</p>
<p style="font-size:14px;text-indent:2em;">
	<span style="line-height:22px;"><span style="font-family:Arial;line-height:28px;"><span style="font-family:" line-height:28px;font-size:medium;"="">八、其他风险：密码失密、操作不当、投资决策失误等原因可能会使交易商发生资金亏损；网上申报、热键操作完毕后未及时退出，他人进行恶意操作而造成的损失；网上交易未及时退出还可能导致遭遇黑客攻击，从而造成损失。<br />
</span></span></span> 
</p>
<p style="font-size:14px;text-indent:2em;">
	<span style="line-height:22px;"><span style="font-family:Arial;line-height:28px;"><span style="font-family:" line-height:28px;font-size:medium;"="">上述风险都将可能会导致交易商发生亏损，该等亏损均由交易商自行承担。在交易商参与普洱茶交易时，他人给予的保证获利或不会发生亏损的任何承诺都是没有根据的，类似的承诺不会减少发生亏损的可能。<br />
</span></span></span><span style="line-height:22px;"><span style="font-family:Arial;line-height:28px;"><span style="font-family:" line-height:28px;font-size:medium;"="">本《风险提示书》提示事项仅为列举，并不能详细列明从事茶叶交易的全部风险和可能影响茶叶市场异常波动的所有情形，其他不可预见的风险因素也可能会给交易商带来损失。交易商在进行茶叶交易前，应确保自己已经做好足够的风险评估和财务安排，避免因参与交易而遭受难以承受的损失。<br />
</span></span></span> 
</p>
<p style="font-size:14px;text-indent:2em;">
	<span style="line-height:22px;"><span style="font-family:Arial;line-height:28px;"><span style="font-family:" line-height:28px;font-size:medium;"="">特别提示：交易中心敬告交易商，应当根据自身的经济条件和心理承受能力认真制定交易策略，尤其是交易商决定购买有较大潜在风险的品种时，更应当清醒地认识到该类实物藏品蕴含的风险。茶叶网上实物交易是一种创新的交易模式。交易商在进行交易时存在盈利的可能，也存在亏损的风险。在您参与茶叶实物交易时，我们郑重地提醒您，认真阅读并谨记以下劝导：<br />
1、请合理配置个人资产，不要以个人全部资产投入茶叶交易市场。<br />
2、请理性管理个人财富，不要拿生活必须资金、自用住房抵押贷款或其他借、贷款投入茶叶市场。<br />
3、请客观评估自己投资抗风险能力，选择恰当的交易品种。<br />
4、请正确运用投资策略，留有适当资金以备不时之需。<br />
5、请认真了解所交易的藏品，不能仅凭市场传言盲目操作。<br />
6、请认真了解并掌握茶叶交易所需的必要知识和相关法规，提高自我保护的能力。<br />
</span></span></span> 
</p>
<p style="font-size:14px;text-indent:2em;">
	<span style="line-height:22px;"><span style="font-family:Arial;line-height:28px;"><span style="font-family:" line-height:28px;font-size:medium;"="">交易中心没有授权任何个人或组织进行茶叶委托投资理财业务，为保护投资人的合法权益，请不要与任何人签订茶叶交易委托代理协议，否则由此引起的一切后果将由您自行承担。<br />
交易中心郑重提醒交易商，请分清投资与投机、风险和收益之间的关系，树立正确的投资理念，谨慎入市，理性操作，注意身心健康与资金安全。<br />
